/**
 * The ScanningOrder enum represents the scanning order options for a specific operation.
 * It defines three constants: ADDING, NAME, and DURATION.
 */
public enum ScanningOrder {
    /**
     * Represents the scanning order of adding, where elements are scanned in the order they were added.
     */
    ADDING,
    /**
     * Represents the scanning order of name, where elements are scanned in alphabetical order by name.
     */
    NAME,
    /**
     * Represents the scanning order of duration, where elements are scanned in ascending order by duration.
     */
    DURATION
}
